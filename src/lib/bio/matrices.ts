export type ScoreMatrix = {
  name: string;
  get: (a: string, b: string) => number;
};

function buildLookup(order: string[], raw: number[][]) {
  const idx = new Map<string, number>();
  order.forEach((c, i) => idx.set(c, i));
  return (a: string, b: string) => {
    const i = idx.get(a.toUpperCase());
    const j = idx.get(b.toUpperCase());
    if (i === undefined || j === undefined) return raw[order.indexOf("X")][order.indexOf("X")];
    return raw[i][j];
  };
}

export const BLOSUM62: ScoreMatrix = {
  name: "BLOSUM62",
  get: buildLookup(BLOSUM62_ORDER, BLOSUM62_RAW)
};

/** Simple DNA identity matrix (+match / −mismatch). */
export function dnaIdentityMatrix(match = 1, mismatch = -1): ScoreMatrix {
  return {
    name: `identity(${match}/${mismatch})`,
    get: (a, b) => (a.toUpperCase() === b.toUpperCase() ? match : mismatch)
  };
}

